<p>A EDKE Marketing Digital é uma empresa que atua no mercado de mídia online, publicidade e propaganda, que assumiu o <strong>Portal de Negócios</strong> com uma proposta inovadora na área de marketing digital, criando parcerias com grandes entidades da indústria, comércio e engenharia.</p>

<p>O objetivo do <strong>Portal de Negócios</strong> é aumentar a visibilidade e a interação entre as empresas, na exposição de produtos/serviços, com foco em novos leads e novas parcerias de negócios.</p>

<h3>Características</h3>		
<p>O <strong>Portal de Negócios</strong> é uma plataforma no segmento de marketing digital, onde são divulgadas as informações de mais de 100 mil empresas associadas. Possui o cadastro de empresas de diversos segmentos como atacadistas, revendedores, fornecedores do comércio, indústria e construção. Além de incluir o Cadastro das Indústrias do Estado do Rio de Janeiro com informações completas sobre o ramo de atividade, CNPJ, endereço, e-mail, etc.</p>

<p>Em espaço único de consulta rápida, direta e objetiva, permite ao anunciante divulgar as informações de sua empresa, bem como seus produtos ou serviços. </p>

<h3>Funcionalidades</h3>	
<p>Para facilitar as pesquisas, o <strong>Portal de Negócios</strong> possui buscas por palavras-chave, descrição dos serviços, fotos de produtos, vídeos e layout responsivo, que possibilita sua pesquisa em qualquer dispositivo com a mesma agilidade. Ferramenta de contato com empresas selecionadas para cotação de produtos e serviços. </p>

<p>São realizadas também, campanhas no Facebook, Instagram e Google Adwords para destacar o <strong>Portal de Negócios</strong>, com intuito de trazer novas interações com seu público alvo e novas possibilidades de parcerias e negócios.</p>

